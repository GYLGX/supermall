<template>
  <div class='toast' v-show='isShow'>
    {{message}}
  </div>
</template>
<script>
  export default {
    name: 'Toast',
    components: {},
    data() {
      return {
        message: '',
        isShow: false
      }
    },
    methods: {
      show(message = '默认文字', duration = 2000) {
        this.isShow = true;
        this.message = message;
        setTimeout(() => {
          this.isShow = false;
          this.message = ''
        }, duration)
      }
    }
  }

</script>
<style scoped>
  .toast {
    position: fixed;
    top: 50%;
    left: 50%;
    transform: translate(-50%, -50%);
    z-index: 999;
    color: #fff;
    background-color: rgba(0, 0, 0, .5);
    font-size: 20px;
    border-radius: 10px;
    padding: 8px 10px;
    text-align: center;
  }

</style>
